"""Виділення чисел

Створи список, який містить усі цілі числа від 1 до 100. Потім знайди всі числа зі списку, які діляться на 7, але не є кратними 5, і збережи їх в окремий список. Наприкінці виведи цей список.

Обмеження: використовуй лише цикл while для перебору елементів."""

import random

lst = list(range(1, 101))
new_lst = []

i = 1
while i < len(lst):
    if lst[i] % 7 == 0 and lst[i] % 5 != 0:
        new_lst.append(lst[i])
    i += 1

#print(len(lst), lst)
print(new_lst)