"""Дано два словники, де ключі - це рядки, а значення - цілі числа. Створіть новий словник,
який міститиме лише ті пари ключ-значення, де ключ присутній в обох словниках, а значення - це сума
значень із двох словників.
"""
dict1 = {"a": 1, "b": 2, "c": 3}
dict2 = {"b": 4, "c": 5, "d": 6}
dict3 = {}

for key in dict1:
    if key in dict2:
        dict3[key] = dict2[key] + dict1[key]
print(dict3)