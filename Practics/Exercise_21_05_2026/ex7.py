"""Дано список кортежів, де кожен кортеж містить два числа (наприклад, [(3, 1), (2, 5),
(4, 2)]). Відсортуйте список за другим числом у кожному кортежі (зростання) вручну за допомогою
циклів.
На виході: # [(3, 1), (4, 2), (2, 5)]"""

lst = [(3, 1), (2, 5),(4, 2)]

for i in range(len(lst)):
    for j in range(len(lst[i])):
        if lst[j][1] > lst[j + 1][1]:
            lst[j], lst[j + 1] = lst[j + 1], lst[j]

print(lst)