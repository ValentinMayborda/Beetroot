"""Гра «Вгадай число»

Напиши програму, яка генерує випадкове число від 1 до 10 та дозволяє користувачу вгадати, яке число було згенероване.
Результат потрібно вивести користувачу за допомогою функції print()."""

import random

a = random.randint(1, 3)
print('Гра «Вгадай число»')

number = int(input('Введи число від 1 до 10: '))

if number == a:
    print(f'Ти вгадав, моє число {a}!')
else:
    print(f'Ти не вгадав моє число! Я загадав {a}')

